class ChangeNodeColor(KatajaAction):
    k_action_uid = 'change_node_color'
    k_command = 'Change node color'
    k_tooltip = 'Change drawing color of nodes'
    k_undoable = False

    def method(self):
        """ Change color for selection or in currently active edge type.
        :return: None
        """
        selector = self.sender()
        if isinstance(selector, ColorDialogForSelector):
            selector = selector.parentWidget()
        color_key = selector.currentData()
        color = ctrl.cm.get(color_key)
        # launch a color dialog if color_id is unknown or clicking
        # already selected color
        update = False
        start = False
        if not color:
            color = ctrl.cm.get('content1')
            ctrl.cm.d[color_key] = color
            start = True
        elif selector.selected_color == color_key:
            start = True
        elif selector.color_dialog:
            update = True
        if start:
            if selector.color_dialog:
                selector.update_color_dialog(color_key)
                selector.color_dialog.show()
            else:
                selector.start_color_dialog(color_key)
        elif update:
            selector.update_color_dialog(color_key)

        selector.selected_color = color_key

        # Update color for selected nodes
        if ctrl.ui.scope_is_selection:
            for node in ctrl.selected:
                if isinstance(node, Node):
                    node.color_id = color_key
                    node.update_label()
        # ... or update color for all nodes of this type
        else:
            ctrl.settings.set_node_setting('color_id', color_key,
                                           node_type=ctrl.ui.active_node_type,
                                           level=FOREST)
            for node in ctrl.forest.nodes.values():
                node.update_label()
        if color_key:
            log.info('(s) Changed node color to: %s' % ctrl.cm.get_color_name(color_key))

    def enabler(self):
        return ctrl.ui.has_nodes_in_scope()

    def getter(self):
        return ctrl.settings.active_nodes('color_id')
